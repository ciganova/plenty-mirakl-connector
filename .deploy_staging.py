def main():
    print(f"[deploy] target {USER}@{HOST}:{REMOTE_DIR}")
    cli = connect()

    # === STEP 1: server prep + sanity ===
    bundle = " && ".join([
        f"docker compose version | head -1",
        f"docker network ls | grep -q traefik_default || (echo MISSING_NETWORK; exit 1)",
        f"ss -ltn | grep -E ':(5432|6379)\\s' | grep -v '127.0.0.1' && echo PORT_LEAK || echo PORT_OK",
        f"df -h /opt | tail -1",
        f"sudo -p '' -S mkdir -p {REMOTE_DIR} /var/backups/pmc-staging",
        f"sudo -p '' -S chown -R {USER}:{USER} {REMOTE_DIR} /var/backups/pmc-staging",
        f"echo PREP_OK",
    ])
    out, rc = run(cli, f"echo {PASS!r} | " + bundle.replace("sudo -p", "sudo -S -p"), quiet=False, timeout=60)
    if "PREP_OK" not in out:
        print("Prep failed!")
        cli.close()
        sys.exit(1)

    # === STEP 2: snapshot existing containers ===
    out, _ = run(cli, "docker ps --format '{{.Names}}'", quiet=True)
    before = set(out.split())
    print(f"[snapshot] {len(before)} containers running before deploy")

    # === STEP 3: rsync-equivalent upload ===
    print(f"[upload] {LOCAL} -> {REMOTE_DIR}")
    sftp = cli.open_sftp()
    upload_dir(sftp, LOCAL, REMOTE_DIR)

    # === STEP 4: write .env.staging ===
    env_text = build_env_text()
    with sftp.file(f"{REMOTE_DIR}/.env.staging", "w") as f:
        f.write(env_text)
    sftp.chmod(f"{REMOTE_DIR}/.env.staging", 0o600)
    print(f"[env] wrote .env.staging ({len(env_text.splitlines())} lines, chmod 600)")
    sftp.close()

    # === STEP 5: build + up ===
    dc = f"docker compose -p {PROJECT} --env-file .env.staging -f {COMPOSE_FILE}"
    out, rc = run(cli, f"cd {REMOTE_DIR} && {dc} build 2>&1 | tail -50", timeout=600)
    if rc != 0:
        print(f"BUILD FAILED rc={rc}")
        cli.close()
        sys.exit(2)

    out, rc = run(cli, f"cd {REMOTE_DIR} && {dc} up -d 2>&1", timeout=180)
    if rc != 0:
        print(f"UP FAILED rc={rc}")
        cli.close()
        sys.exit(3)

    # === STEP 6: wait for healthcheck (poll inside container, then via traefik) ===
    print("[health] waiting for api healthz...")
    for i in range(30):
        out, _ = run(cli, f"cd {REMOTE_DIR} && {dc} exec -T api curl -fsS http://localhost:8000/healthz 2>&1 || echo WAIT", quiet=True, timeout=15)
        if '"ok":true' in out or '"ok": true' in out:
            print(f"  internal healthz OK after {i*3}s")
            break
        time.sleep(3)
    else:
        print("  internal healthz did NOT come up in 90s — dumping logs")
        run(cli, f"cd {REMOTE_DIR} && {dc} logs api --tail=80")
        cli.close()
        sys.exit(4)

    # === STEP 7: migrations ran via compose command (alembic upgrade head) — verify ===
    out, _ = run(cli, f"cd {REMOTE_DIR} && {dc} exec -T api alembic current 2>&1", quiet=True, timeout=30)
    print(f"[alembic] current: {out.strip()[-300:]}")

    # === STEP 8: external healthcheck via Traefik ===
    print("[health] external HTTPS healthz (allows up to 60s for cert)...")
    for i in range(20):
        out, _ = run(cli, f"curl -fsS -o /tmp/h.txt -w '%{{http_code}}' https://{DOMAIN}/healthz 2>&1 || echo FAIL",
                    quiet=True, timeout=15)
        if "200" in out:
            print(f"  external HTTPS /healthz 200 after {i*3}s")
            break
        time.sleep(3)
    else:
        print(f"  external HTTPS NOT reachable in 60s; last: {out.strip()}")

    # === STEP 9: verify others not affected ===
    out, _ = run(cli, "docker ps --format '{{.Names}}'", quiet=True)
    after = set(out.split())
    missing = before - after
    if missing:
        print(f"!!! WARN: containers MISSING after deploy: {sorted(missing)}")
    else:
        print(f"[verify] all {len(before)} pre-existing containers still running.")

    # === STEP 10: verify landing serves ===
    out, _ = run(cli, f"curl -fsS https://{DOMAIN}/ 2>&1 | head -c 400 || echo FAIL", quiet=True, timeout=10)
    landing_ok = "PlentyMirakl" in out or "plenty" in out.lower()
    print(f"[landing] reachable: {landing_ok}")
    print(out[:300])

    cli.close()
    print("\n[deploy] DONE")
    return 0


if __name__ == "__main__":
    sys.exit(main())
