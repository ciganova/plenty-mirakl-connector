#!/usr/bin/env python3
"""One-shot staging deploy for plenty-mirakl-connector.

Single SSH session: server prep + rsync-equivalent + write .env.staging + build + up + migrate + healthcheck.
"""
from __future__ import annotations
import os
import sys
import time
from pathlib import Path

import paramiko

# --- creds ---
def load_env(path: str) -> dict:
    p = Path(path).expanduser()
    env: dict[str, str] = {}
    if p.is_file():
        for line in p.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue
            k, v = line.split("=", 1)
            env.setdefault(k.strip(), v.strip())
    return env

SBOX = load_env("~/.claude/credentials/servicebox.env")
SECRETS = load_env("~/.claude/credentials/plenty-mirakl-staging.env")
PLENTY = load_env("~/.claude/credentials/plenty-test-p73736.env")
MIRAKL = load_env("~/.claude/credentials/mirakl-douglas2-dev.env")

HOST = SBOX["SERVER_IP"]
USER = SBOX["SSH_USER"]
PASS = SBOX["SSH_PASS"]
PORT = int(SBOX.get("SSH_PORT", "22"))

REMOTE_DIR = "/opt/plenty-mirakl-staging"
COMPOSE_FILE = "docker-compose.staging.yml"
PROJECT = "pmc-staging"
DOMAIN = "staging.plenty-mirakl.420.ovh"

LOCAL = Path(__file__).parent.resolve()


def connect() -> paramiko.SSHClient:
    cli = paramiko.SSHClient()
    cli.set_missing_host_key_policy(paramiko.AutoAddPolicy())
    cli.connect(HOST, port=PORT, username=USER, password=PASS,
                look_for_keys=False, allow_agent=False, timeout=30)
    return cli


def run(cli, cmd, sudo=False, quiet=False, timeout=300):
    if sudo:
        full = f"echo {PASS!r} | sudo -S -p '' bash -c {cmd!r}"
    else:
        full = cmd
    if not quiet:
        print(f"$ {cmd[:160]}{'...' if len(cmd) > 160 else ''}")
    chan = cli.get_transport().open_session()
    chan.set_combine_stderr(True)
    chan.settimeout(timeout)
    chan.exec_command(full)
    chunks = []
    deadline = time.time() + timeout
    while True:
        if chan.recv_ready():
            c = chan.recv(8192)
            if c:
                chunks.append(c.decode(errors="replace"))
        if chan.exit_status_ready() and not chan.recv_ready():
            break
        if time.time() > deadline:
            break
        time.sleep(0.1)
    while chan.recv_ready():
        chunks.append(chan.recv(8192).decode(errors="replace"))
    out = "".join(chunks)
    rc = chan.recv_exit_status()
    if not quiet:
        try:
            print(out.rstrip()[-2000:])
        except UnicodeEncodeError:
            print(out.rstrip()[-2000:].encode("ascii", "replace").decode())
    return out, rc


def upload_dir(sftp, local: Path, remote: str):
    skip_dirs = {".git", ".venv", ".venv-deploy", "venv", "__pycache__", "node_modules",
                 ".idea", ".vscode", "data", "logs", ".pytest_cache", "htmlcov", "dist", "build"}
    skip_files_prefix = {".env"}  # never copy local .env*
    skip_files_exact = {".DS_Store"}
    allowed_dotfiles = {".gitignore", ".env.example"}

    # ensure remote root
    try:
        sftp.stat(remote)
    except IOError:
        sftp.mkdir(remote)

    for entry in sorted(local.rglob("*")):
        rel = entry.relative_to(local)
        parts = rel.parts
        if any(p in skip_dirs for p in parts):
            continue
        if entry.name.startswith(".") and entry.name not in allowed_dotfiles:
            if any(entry.name.startswith(pfx) for pfx in skip_files_prefix):
                continue
            if entry.is_file() and entry.name not in allowed_dotfiles:
                continue
        if entry.name in skip_files_exact:
            continue
        rpath = f"{remote}/{rel.as_posix()}"
        if entry.is_dir():
            try:
                sftp.mkdir(rpath)
            except IOError:
                pass
        else:
            sftp.put(str(entry), rpath)


